/**
 * Compute the quotient of two numbers.
 *
 * @since 0.1.0
 * @param {number} dividend A number which is divided by another number.
 * @param {number} divisor A number by which another number is divided.
 * @returns {number} Returns the quotient.
 *
 * @example
 * divide(9,3)
 * // => 3
 */
export default function divide(dividend: number, divisor: number): number {
  return dividend / divisor;
}
