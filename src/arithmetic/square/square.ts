/**
 * Compute the value of a base expression squared.
 *
 * @since 0.5.0
 * @param {number} x The base value of the expression.
 * @returns {number} Returns the base expression squared.
 *
 * @example
 * square(4)
 * // => 16
 */
export default function square(x: number): number {
  return x * x;
}
