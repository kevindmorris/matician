/**
 * Returns the square of a number.
 *
 * @param value - The number to square.
 * @returns The square of the given number.
 *
 * @example
 * ```ts
 * square(3);    // returns 9
 * square(-4);   // returns 16
 * square(0);    // returns 0
 * ```
 */
export default function square(value: number): number {
  return value * value;
}
