/**
 * Adds two numbers together.
 *
 * @param a - The first number.
 * @param b - The second number.
 * @returns The sum of `a` and `b`.
 *
 * @example
 * ```ts
 * add(2, 3); // returns 5
 * add(-1, 1); // returns 0
 * ```
 */
export default function add(a: number, b: number): number {
  return a + b;
}
